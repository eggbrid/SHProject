public class SkillB {
    private int id;
    private String name;
    private String info;
    private int useMp;
    private int hitHp;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public int getUseMp() {
        return useMp;
    }

    public void setUseMp(int useMp) {
        this.useMp = useMp;
    }

    public int getHitHp() {
        return hitHp;
    }

    public void setHitHp(int hitHp) {
        this.hitHp = hitHp;
    }
}
